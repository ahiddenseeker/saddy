sad::Maybe<sad::AColor> sad::util::Markup::getColorFromTable(const char* s)
{
    int index = 0;
    while(color_names[index])
    {
        if (!strcmp(s, color_names[index]))
        {
            return sad::Maybe<sad::AColor>(colors[index]);
        }
        ++index;
    }
    return sad::Maybe<sad::AColor>();
}
